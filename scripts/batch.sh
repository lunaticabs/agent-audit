MAX_JOBS="${MAX_JOBS:-6}"
LOG_DIR="${LOG_DIR:-batch_logs}"
mkdir -p "$LOG_DIR"
run_one() {
  local addr="$1"
  local log_file="$LOG_DIR/${addr}.log"
  {
    echo "[START] $addr $(date '+%F %T')"
    opencode run --attach http://127.0.0.1:4096 --agent audit -m apiapi/gpt-5.3-codex "Audit $addr on eth."
    ec=$?
    echo "[DONE ] $addr exit=$ec $(date '+%F %T')"
    return $ec
  } >"$log_file" 2>&1
}
export -f run_one
export LOG_DIR
